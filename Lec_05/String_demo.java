package Lec_05;

public class String_demo {
	public static void main(String[] args) {

		System.out.println("Hello" + 10);
		System.out.println("Hello" + 10 + 20);
		System.out.println(10 + "Hello" + 10 + 20);
		System.out.println(20 + 10 + "Hello" + 10 + 20);
		System.out.println(20 + 10 + "Hello" + (10 + 20));
	}
}
